#include "3-calc.h"

/**
 * op_add - add two ints
 * @a: first input
 * @b: second input
 * Return: the sum.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two ints
 * @a: first input
 * @b: second input
 * Return: the substraction.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two ints
 * @a: first input
 * @b: second input
 * Return: the product.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two ints
 * @a: first input
 * @b: second input
 * Return: result of the division.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of two ints
 * @a: first input
 * @b: second input
 * Return: reminder of the division.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
